    current_pressure_mb DECIMAL(6, 1),
    current_pressure_in DECIMAL(5, 2),
    current_precip_mm DECIMAL(6, 2),
    current_precip_in DECIMAL(5, 2),
    current_humidity INT,
    current_cloud INT,
    current_feelslike_c DECIMAL(5, 2),
    current_feelslike_f DECIMAL(5, 2),
    current_windchill_c DECIMAL(5, 2),
    current_windchill_f DECIMAL(5, 2),
    current_heatindex_c DECIMAL(5, 2),
    current_heatindex_f DECIMAL(5, 2),
