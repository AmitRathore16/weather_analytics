    current_uv DECIMAL(4, 1),
    current_gust_mph DECIMAL(5, 2),
    current_gust_kph DECIMAL(5, 2),
    current_will_it_rain TINYINT,
    current_chance_of_rain INT,
    current_will_it_snow TINYINT,
    current_chance_of_snow INT,
    current_short_rad DECIMAL(14, 2),
    current_diff_rad DECIMAL(14, 2),
    current_dni DECIMAL(14, 2),
    current_gti DECIMAL(14, 2),
    current_aqi_co DECIMAL(12, 6),
    current_aqi_no2 DECIMAL(12, 6),
