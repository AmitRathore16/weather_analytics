    current_temp_f DECIMAL(5, 2),
    current_is_day TINYINT,
    current_condition_text VARCHAR(255),
    current_condition_icon VARCHAR(255),
    current_condition_code INT,
    current_wind_mph DECIMAL(5, 2),
    current_wind_kph DECIMAL(5, 2),
    current_wind_degree INT,
